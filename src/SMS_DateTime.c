void _addDate ( void* );
__asm__(
 ".set noreorder\n\t"
 ".set nomacro\n\t"
 ".set noat\n\t"
 ".text\n\t"
 "_addDate:\n\t"
 "or    $a1, $zero, $a0\n\t"
 "addiu $sp, $sp, -16\n\t"
 "lbu   $v1, 5($a1)\n\t"
 "lui   $v0, %hi( s_Const )\n\t"
 "lbu   $a0, 7($a1)\n\t"
 "addiu $v1, $v1, 1\n\t"
 "addiu $t1, $v0, %lo( s_Const )\n\t"
 "ld    $a2, 0($t1)\n\t"
 "lw    $a3, 8($t1)\n\t"
 "sd    $a2, 0($sp)\n\t"
 "sw    $a3, 8($sp)\n\t"
 "andi  $a0, $a0, 3\n\t"
 "bnez  $a0, 1f\n\t"
 "sb    $v1, 5($a1)\n\t"
 "ori   $v0, $zero, 29\n\t"
 "sb    $v0, 1($sp)\n\t"
 "1:\n\t"
 "lbu   $v0, 6($a1)\n\t"
 "lbu   $a0, 5($a1)\n\t"
 "addu  $v0, $v0, $sp\n\t"
 "lb    $v1, -1($v0)\n\t"
 "slt   $v1, $v1, $a0\n\t"
 "beqz  $v1, 2f\n\t"
 "ori   $v0, $zero, 1\n\t"
 "lbu   $v1, 6($a1)\n\t"
 "sb    $v0, 5($a1)\n\t"
 "ori   $a0, $zero, 13\n\t"
 "addiu $v1, $v1, 1\n\t"
 "andi  $v0, $v1, 255\n\t"
 "bne   $v0, $a0, 2f\n\t"
 "sb    $v1, 6($a1)\n\t"
 "lbu   $v1, 7($a1)\n\t"
 "ori   $v0, $zero, 99\n\t"
 "bne   $v1, $v0, 3f\n\t"
 "addiu $v0, $v1, 1\n\t"
 "beq   $zero, $zero, 4f\n\t"
 "sb    $zero, 7($a1)\n\t"
 "3:\n\t"
 "sb    $v0, 7($a1)\n\t"
 "4:\n\t"
 "ori   $v0, $zero, 1\n\t"
 "sb    $v0, 6($a1)\n\t"
 "2:\n\t"
 "jr    $ra\n\t"
 "addiu $sp, $sp, 16\n\t"
 ".set at\n\t"
 ".set macro\n\t"
 ".set reorder\n\t"
);

