void _clockFromBCD ( void* );
__asm__(
 ".set noreorder\n\t"
 ".set nomacro\n\t"
 ".set noat\n\t"
 ".text\n\t"
 "_clockFromBCD:\n\t"
 "pcpyld    $ra, $ra, $ra\n\t"
 "or        $at, $zero, $a0\n\t"
 "bgezal    $zero, _fromBCD\n\t"
 "lbu       $a0, 7($a0)\n\t"
 "lbu       $a0, 6($at)\n\t"
 "bgezal    $zero, _fromBCD\n\t"
 "sb        $v0, 7($at)\n\t"
 "lbu       $a0, 5($at)\n\t"
 "bgezal    $zero, _fromBCD\n\t"
 "sb        $v0, 6($at)\n\t"
 "lbu       $a0, 3($at)\n\t"
 "bgezal    $zero, _fromBCD\n\t"
 "sb        $v0, 5($at)\n\t"
 "lbu       $a0, 2($at)\n\t"
 "bgezal    $zero, _fromBCD\n\t"
 "sb        $v0, 3($at)\n\t"
 "lbu       $a0, 1($at)\n\t"
 "bgezal    $zero, _fromBCD\n\t"
 "sb        $v0, 2($at)\n\t"
 "pcpyud    $ra, $ra, $ra\n\t"
 "jr        $ra\n\t"
 "sb        $v0, 1($at)\n\t"
 ".set at\n\t"
 ".set macro\n\t"
 ".set reorder\n\t"
);

