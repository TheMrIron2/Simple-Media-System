void _adjustTime ( void*, int );
__asm__(
 ".set noreorder\n\t"
 ".set nomacro\n\t"
 ".set noat\n\t"
 ".text\n\t"
 "_adjustTime:\n\t"
 "addiu     $sp, $sp, -16\n\t"
 "sw        $ra, 0($sp)\n\t"
 "or        $t2, $zero, $a0\n\t"
 "bgezal    $zero, _clockFromBCD\n\t"
 "or        $t3, $zero, $a1\n\t"
 "lbu       $v0, 2($t2)\n\t"
 "addu      $t3, $v0, $t3\n\t"
 "bltzl     $t3, 1f\n\t"
 "addiu     $t3, $t3, 60\n\t"
 "slti      $v0, $t3, 60\n\t"
 "bnezl     $v0, 2f\n\t"
 "sb        $t3, 2($t2)\n\t"
 "or        $a0, $zero, $t2\n\t"
 "nop\n\t"
 "3:\n\t"
 "bgezal    $zero, _addHour\n\t"
 "addiu     $t3, $t3, -60\n\t"
 "slti      $v0, $t3, 60\n\t"
 "beqz      $v0, 3b\n\t"
 "or        $a0, $zero, $t2\n\t"
 "beq       $zero, $zero, 4f\n\t"
 "sb        $t3, 2($t2)\n\t"
 "1:\n\t"
 "bgezal    $zero, _subHour\n\t"
 "or        $a0, $zero, $t2\n\t"
 "bltzl     $t3, 1b\n\t"
 "addiu     $t3, $t3, 60\n\t"
 "sb        $t3, 2($t2)\n\t"
 "2:\n\t"
 "or        $a0, $zero, $t2\n\t"
 "4:\n\t"
 "lw        $ra, 0($sp)\n\t"
 "jr        $ra\n\t"
 "addiu     $sp, $sp, 16\n\t"
 ".set at\n\t"
 ".set macro\n\t"
 ".set reorder\n\t"
);

